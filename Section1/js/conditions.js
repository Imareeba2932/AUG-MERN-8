// There are three types of conditions in JavaScript
// 1. if statement
// 2. if-else statement
// 3. if-else-if statement
// 4. switch statement

// if statement
let age = 18
if (age >= 18) {
    console.log("You can vote")
} if (age === 18) {
    console.log("You are 18 so you also can vote")
} if (age < 18) {
    console.log("You can't vote")
}

console.log("---------------------------")

// if-else-if statement
let age1 = 18
if (age1 >= 18) {
    console.log("You can vote")
} else if (age1 === 18) {
    console.log("You are 18 so you also can vote")
} else {
    console.log("You can't vote")
}
